public class Two_D_Array {
    public static void main(String args[])
    {

        int arr[][] = {{9,8,7},{6,5,4},{3,2,1}};
        for (int i=0; i<3; i++)
        {
            for (int j=0; j<3; j++)
            {
                System.out.println(arr[i][j]+" ");

            }
            System.out.println("End of Index of Array");
        }

    }
}
