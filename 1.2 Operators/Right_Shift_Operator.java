public class Right_Shift_Operator {
    public static void main(String[] args)
    {
        int Num = 12;
        System.out.println("Left Shift : 12>>1 "+(Num>>1));
        System.out.println("Left Shift : 12>>2 "+(Num>>2));
        System.out.println("Left Shift : 12>>3 "+(Num>>3));
    }
}
